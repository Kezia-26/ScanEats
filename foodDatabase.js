  export const analyzeFood = (food, healthIssues) => {
    let rating = 8;
    const warnings = [...food.warnings];
  
    // Sugar analysis
    if (healthIssues.sugar) {
      if (food.nutritionalInfo.sugar > 10) {
        rating -= 3;
        warnings.push('Very high sugar content - Not recommended');
      } else if (food.nutritionalInfo.sugar > 5) {
        rating -= 2;
        warnings.push('Moderate sugar content - Consume in moderation');
      }
    }
  
    // Blood pressure analysis
    if (healthIssues.bloodPressure) {
      if (food.nutritionalInfo.sodium > 500) {
        rating -= 3;
        warnings.push('Very high sodium content - Not recommended');
      } else if (food.nutritionalInfo.sodium > 140) {
        rating -= 2;
        warnings.push('Moderate sodium content - Consume in moderation');
      }
    }
  
    // Digestion analysis
    if (healthIssues.digestion) {
      if (food.ingredients.some(i => 
        ['MSG', 'Artificial Flavors', 'Preservatives'].includes(i))) {
        rating -= 2;
        warnings.push('Contains ingredients that may affect digestion');
      }
    }
  
    // Heart disease analysis
    if (healthIssues.heartDisease) {
      if (food.nutritionalInfo.fat > 15) {
        rating -= 3;
        warnings.push('High fat content - Not recommended for heart patients');
      }
    }
  
    return {
      rating: Math.max(1, rating),
      warnings: [...new Set(warnings)], // Remove duplicate warnings
      isHealthy: rating >= 6,
      alternatives: food.alternatives
    };
  };
